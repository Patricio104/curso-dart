/**
 * Un operador es un símbolo que le dice al compilador
 * que debe de realizar una tarea
 * matemática, relacional o lógica
 * y debe de producir un resultado
 */
main() {
  //operadores aritméticos básicos

  int a = 10 + 5; // +15
  a = 20 - 10; // - 10
  a = 10 * 2; // * 20

  double b = 10 / 2; // / 5

  b = 10 % 3; // % 1 El sobrante de la división
  b = -b; // -exp Es usado para cambiar el valor de la expresión

  int c = 10 ~/ 3; // 3 División común el resultado es un numero entero

  int d = 1;
  d++; // 2 Incremento
  d--; // 1 Decremento

  d += 2; // +=  3
  d -= 2; //  -= 1  *=  /=
}
